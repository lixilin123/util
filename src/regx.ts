/**
 * 常用正则表达式
 */
 const regx = {
  /** 手机号码 */
  mobile: /^1[0-9]{10}$/,
  /** 全部是中文 */
  chinese: /^[\u4E00-\u9FA5]+$/,
  /** 简单的身份证 */  
  card: /(^\d{15}$)|(^\d{18}$)|(^\d{17}(\d|X)$)/, 
  /** 邮箱 */    
  email: /^[a-zA-Z0-9.!#$%&'*+\/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$/,
  /** 整数 */    
  digits: /^\d+$/
}

export default regx;
